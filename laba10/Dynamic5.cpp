#include <iostream>
#include "Dynamic5.h"
#include "stack.h"

void Dynamic5() {
    using namespace std;
    // Создаем пустой стек (вызывается конструктор по умолчанию)
    Stack stack;

    // Меню выбора способа заполнения стека (3 способа по заданию)
    cout << "Выберите способ создания стека:" << endl;
    cout << "1 - с клавиатуры" << endl;
    cout << "2 - из файла" << endl;
    cout << "3 - случайно" << endl;
    cout << "Ваш выбор: ";

    int choice;
    cin >> choice;

    // Обработка выбора пользователя
    switch (choice) {
    case 1:  // Ввод с клавиатуры
        stack.fillFromKeyboard();  // Пользователь вводит элементы вручную
        break;

    case 2: {  // Чтение из файла
        string filename;
        cout << "Введите имя файла: ";
        cin >> filename;
        stack.fillFromFile(filename);  // Чтение чисел из файла
        break;
    }

    case 3: {  // Случайная генерация
        int n;
        cout << "Введите количество элементов: ";
        cin >> n;
        stack.fillRandom(n);  // Генерация случайных чисел
        break;
    }

    default:  // Неверный выбор
        cout << "Неверный выбор. Создан пустой стек." << endl;
        return;  // Выход из функции
    }

    // Проверка на не пустоту стека
    if (stack.isEmpty()) {
        cout << "Ошибка: стек должен быть непустым!" << endl;
        return;  // Выход из функции, если стек пуст
    }

    // Вывод исходного состояния стека
    cout << "\nИсходный ";
    stack.print();

    // Получаем адрес вершины
    Node* P1 = stack.getTop();
    cout << "P1 (адрес вершины ДО извлечения): " << P1 << endl;


    // Извлекаем верхний элемент
    int D = stack.pop();

    // P2 - указатель на новую вершину после извлечения
    Node* P2 = stack.getTop();

    cout << "\nРезультат:" << endl;
    cout << "Извлеченное значение D = " << D << endl;
    cout << "P2 (адрес новой вершины): " << P2;

    // Обработка случая, когда стек стал пустым после извлечения
    if (P2 == nullptr) {
        cout << " (стек стал пустым)";
    }
    else {
        // Если стек не пуст, выводим значение новой вершины
        cout << ", значение новой вершины: " << stack.peek();
    }
    cout << endl;

    // Вывод состояния стека после извлечения
    cout << "\nПосле извлечения ";
    stack.print();

    // Демонстрация поиска по значению
    cout << "\nВведите число которое нужно найти: ";
    int n;
    cin >> n;
    // Поиск элемента по значению
    stack.find(n);  
    cout << endl;

    // Демонстрация копирования
    cout << "Демонстрация копирования" << endl;
    cout << "Создаем копию стека:" << endl;

    // Демонстрация конструктора копирования
    Stack stackCopy = stack;

    cout << "Оригинальный стек: ";
    stack.print();
    cout << "Копия стека: ";
    stackCopy.print();

    // Демонстрация независимости копии от оригинала
    cout << "\nИзменяем оригинал (удаляем верхний элемент)" << endl;
    stack.pop();  // Удаляем элемент из оригинала

    cout << "Оригинальный стек (изменился): ";
    stack.print();
    cout << "Копия стека (не изменилась): ";
    stackCopy.print();
    cout << endl;

    // После завершения функции автоматически запускается деструктор
}