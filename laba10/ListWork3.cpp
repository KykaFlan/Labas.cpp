#include <iostream>
#include "ListWork3.h"
#include "list.h"

void ListWork3() {
    using namespace std;
    // Создаем пустой список (конструктор по умолчанию)
    LinkedList list;

    // Меню выбора способа заполнения списка
    cout << "Выберите способ создания списка:" << endl;
    cout << "1 - с клавиатуры" << endl;
    cout << "2 - из файла" << endl;
    cout << "3 - случайно (минимум 5 элементов)" << endl;
    cout << "Ваш выбор: ";

    int choice;
    cin >> choice;

    // Обработка выбора пользователя
    switch (choice) {
    case 1:  // Ввод с клавиатуры
        list.fillFromKeyboard();  // Пользователь вводит элементы вручную
        break;

    case 2: {  // Чтение из файла
        string filename;
        cout << "Введите имя файла: ";
        cin >> filename;
        list.fillFromFile(filename);  // Чтение чисел из файла
        break;
    }

    case 3: {  // Случайная генерация
        int n;
        cout << "Введите количество элементов (минимум 5): ";
        cin >> n;
        if (n < 5) {
            cout << "Ошибка: нужно минимум 5 элементов" << endl;
            return;
        }
        list.fillRandom(n);  // Генерация случайных чисел
        break;
    }

    default:  // Неверный выбор
        cout << "Неверный выбор." << endl;
        return;  // Выход из функции
    }

    // Проверяем на не пустоту списка
    Node* p1 = list.getHead();
    if (p1 == nullptr) {
        cout << "Ошибка: список пуст!" << endl;
        return;  // Выход из функции
    }

    // Подсчет элементов для проверки условия
    int count = 0;
    Node* cur = p1;
    while (cur) {
        count++;
        cur = cur->next;
    }

    if (count < 5) {
        cout << "Ошибка: в списке менее 5 элементов" << endl;
        return;  // Выход из функции
    }

    // Вывод исходного состояния
    cout << "\nИсходный ";
    list.print();  // Выводим содержимое списка
    cout << "P1 (адрес головы): " << p1 << ", значение: " << p1->data << endl;

    // Получаем указатель на 4-й элемент
    Node* p4 = list.getNthNode(4);

    // Вывод результата
    cout << "\nРезультат:" << endl;
    cout << "P4 (адрес 4-го элемента): " << p4;
    cout << ", значение элемента: " << p4->data << endl;

    // Демонстрация поиска по значению
    cout << "\nВведите число которое нужно найти: ";
    int n;
    cin >> n;
    list.find(n);
    cout << endl;

    // Демонстрация конструктора копирования
    cout << "Демонстрация копирования" << endl;
    cout << "Создаем копию списка:" << endl;

    LinkedList listCopy = list;

    cout << "Оригинальный список: ";
    list.print();
    cout << "Копия списка: ";
    listCopy.print();

    // Демонстрация независимости копии от оригинала
    cout << "\nИзменяем оригинал (добавляем элемент в конец '1337')" << endl;
    list.append(1337);  // Добавляем элемент в оригинал

    cout << "Оригинальный список (изменился): ";
    list.print();
    cout << "Копия списка (не изменилась): ";
    listCopy.print();
    cout << endl;

    // После завершения функции автоматически запускается деструктора
}